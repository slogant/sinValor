    private void cargaValores() {
       
        controlVariable.setVisible(false);
        valorTexto.setVisible(false);
        elNombreSucursal.setText(BaseNombreSuc);
        var mexico = forLanguageTag("es-MX");
        var anio = "dd/MM/yyyy";
        var tiempo = now();
        var valorFecha = LocalDate.now().format(ofPattern(anio, mexico));
        daNombre.setText(unNombre);
        fechaSistema.setText(valorFecha);
        jDesktopPane1.setBorder(new FondoDesktop());
        temas = new JMenu("Estilo");
        JMenu controlMenu = new JMenu();
        JMenuItem subItemsMenu = new JMenuItem();
        var roles = Long.valueOf(mas);
        var enviado = Long.valueOf(emas);
        ControlDeMenu.obtenerMenus(enviado, roles, true, jDesktopPane1, barraMenu, controlMenu, subItemsMenu);
        barraMenu.add(controlMenu);
        controlMenu.add(subItemsMenu);
        barraMenu.add(temas);
        revalidate();
        repaint();
        muestra.addActionListener(e -> {
            creandoUsuario = null;
            out.println("Dialogo destruido");
            var g = (creandoUsuario == null);
            out.println("Cerrado correctamente " + g);
            if (g && creandoUsuario == null) {
                creandoUsuario = new CrearUsuario();
                var pantalla = getDefaultToolkit().getScreenSize();
                var ventana = creandoUsuario.getSize();
                var x = ((pantalla.width - ventana.width) / 2);
                var y = ((pantalla.height - ventana.height) / 2);
                creandoUsuario.setLocation(x, y);
                creandoUsuario.setVisible(true);
                jDesktopPane1.add(creandoUsuario);
                var control = controlVariable.getText();
                showMessageDialog(this, control);
            }
        });
        /*Toolkit tk = Toolkit.getDefaultToolkit();
         int xSize = ((int) tk.getScreenSize().getWidth());
         int ySize = ((int) tk.getScreenSize().getHeight());
         setSize(new Dimension(xSize, ySize));*/
        cerrado.addActionListener(e -> {
            var close = showInternalConfirmDialog(jDesktopPane1, "Desea cerrar la aplicaciÃ³n", "Monitor", YES_NO_OPTION);
            if (close == YES_OPTION) {
                terminado = registraSalida(enviado, roles);
                if (terminado) {
                    out.println("Control de aplicaciones terminado");
                    //new Dissolver().dissolveExit(this);
                    exit(0);
                } else {
                    out.println("No se guardo la salida");
                    exit(0);
                }
            } else {
                showInternalMessageDialog(jDesktopPane1, "Cancelado salida del programa", "Monitor", INFORMATION_MESSAGE);
            }
        });
    }

    private void cargaTemas() {
        for (var info : getInstalledLookAndFeels()) {
            var nombreTema = info.getName();
            var nombreClase = info.getClassName();
            JMenuItem itemTema = new JMenuItem(nombreTema);
            temas.add(itemTema);
            temas.addSeparator();
            itemTema.addActionListener(e -> {
                try {
                    var nombre = e.getActionCommand();
                    if (nombreTema.equals(nombre)) {
                        setLookAndFeel(nombreClase);
                        updateComponentTreeUI(this);
                        out.println(nombre);
                    }
                } catch (ClassNotFoundException | InstantiationException | IllegalAccessException | UnsupportedLookAndFeelException ex) {
                    Logger.getLogger(CentroPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    out.println(ex.getLocalizedMessage());
                }
            });
        }
    }

    private void IniciaReloj() {
        ActionListener al = (e) -> cargarHoraLocal();
        javax.swing.Timer eltime = new javax.swing.Timer(1000, al);
        eltime.start();
    }

    private void cargarHoraLocal() {
        var tie = now();
        DateTimeFormatter fmts = ofPattern("KK:mm:ss a");
        Horas = fmts.format(tie);
        laHora.setText(Horas);
    }

    private void cargaLaImagenPanel() {
        panelFotografia = new panelFoto();
        new Explosion(panelContenedor, panelFotografia).play();
    }

